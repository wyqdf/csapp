# -------------------------- 段声明与入口点 --------------------------
.section .text          # 定义代码段（.text）：存放可执行指令，只读且可执行

.global _start          # 声明全局符号 _start：链接器(ld)识别的程序入口点，必须全局可见

# -------------------------- 程序入口：_start --------------------------
_start:                 # 程序入口标签：32位ELF程序默认从这里开始执行
    nop                 # 空操作指令（No Operation）：仅占位/调试对齐用，不改变寄存器/内存状态

    # -------------------------- 寄存器赋值（演示数据类型） --------------------------
    movl $4, %eax       # movl：l=long(32位) | 把立即数4存入32位寄存器eax
                         # 注：32位Linux系统调用中，eax用于存系统调用号（4对应write，但此处未完整实现write）

    movw $1, %bx        # movw：w=word(16位) | 把立即数1存入16位寄存器bx
                         # 注：bx是32位寄存器ebx的低16位，修改bx会影响ebx的低16位，高16位不变

    movb $0xff65, %dh   # movb：b=byte(8位) | 把立即数0xff65存入8位寄存器dh
                         # ⚠️ 关键：dh是8位寄存器，只能存1字节！0xff65会被截断为低8位0x65
                         # 注：dh是32位寄存器edx的高8位（edx结构：dh(高8位)+dl(低8位)=dx(低16位)，dx+高16位=edx）

    movl $13, %edx      # movl：32位赋值 | 把立即数13存入32位寄存器edx
                         # 注：write系统调用中，edx用于存“写入字节数”，此处仅演示赋值

    # -------------------------- 内存地址操作 --------------------------
    movl $0x08048055,%ecx  # movl：32位赋值 | 把内存地址0x08048055存入32位寄存器ecx
                             # 注：0x08048055是32位ELF程序的经典“数据段/代码段”地址范围
                             # 注：write系统调用中，ecx用于存“缓冲区内存地址”，此处仅演示地址赋值

    movl (%ecx),%eax    # movl：32位内存读取 | 把ecx指向的内存地址(0x08048055)中的32位值，读取到eax
                         # 注：() 是AT&T语法的“间接寻址”标记，(%ecx) = 以ecx的值为地址，取该地址的内容
                         # ⚠️ 注意：0x08048055是硬编码地址，若该地址未映射/无权限，程序会段错误(Segfault)

    # -------------------------- 退出程序（系统调用） --------------------------
    movl $1, %eax       # movl：32位赋值 | 把系统调用号1存入eax（1对应exit系统调用）
    movl $0, %ebx       # movl：32位赋值 | 把exit的参数0存入ebx（0=程序正常退出，非0=错误码）
    int $0x80           # 触发32位Linux系统调用中断
                         # 注：int=interrupt(中断)，$0x80是32位Linux专用的系统调用中断向量号
                         # 内核会根据eax的值(1)执行exit系统调用，终止当前进程